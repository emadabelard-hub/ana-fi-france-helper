export const getMetiers = (sectorSlug: string | null | undefined): MetierDef[] =>
  (sectorSlug && OPPORTUNITE_METIERS[sectorSlug]) || [];

export const findMetier = (sectorSlug: string | null | undefined, metierSlug: string | null | undefined): MetierDef | undefined =>
  getMetiers(sectorSlug).find((m) => m.slug === metierSlug);

export const findSector = (slug: string | null | undefined): SectorDef | undefined =>
  OPPORTUNITE_SECTORS.find((s) => s.slug === slug);
